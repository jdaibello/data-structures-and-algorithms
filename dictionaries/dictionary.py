# Creating Dictionary

myDict = dict()
print(myDict)

myDict2 = {}
print(myDict2)

engSp = dict(one='uno', two='dos', three='tres')
print(engSp)

engSp2 = {'one': 'uno', 'two': 'dos', 'three': 'tres'}
print(engSp2)

engSpList = [('one', 'uno'), ('two', 'dos'), ('three', 'tres')]
print(dict(engSpList))